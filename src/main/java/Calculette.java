public class Calculette {
    public Calculette()
    {
    }

    public double additionner(double a, double b)
    {
        return a + b;
    }

    public double soustraire(double a, double b)
    {
        return a - a;
    }

    public double multiplier(double a, double b)
    {
        return a * b;
    }

    public double diviser(double a, double b)
    {
        return a / b;
    }
}

